import SwiftUI

extension Color {
    enum Gradient {
        static let blue = Color("GradientBlue")
        static let lavender = Color("GradientLavender")
        static let mint = Color("GradientMint")
        static let peach = Color("GradientPeach")
        static let yellow = Color("GradientYellow")
    }
}
